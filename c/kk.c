#include <unistd.h>

int	main(int ac, char **av)
{
	int i = 0;
	while (av[1][i])
	{
		if (av[1][i] == 'z')
			av[1][i] = 'a';
		if (av[1][i] >= 'a' && av[1][i] <= 'y')
			av[1][i] += 1;
		write(1, &av[1][i], 1);
		i++;
	}
}