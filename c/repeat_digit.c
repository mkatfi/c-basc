#include <unistd.h>

int	main(int ac, char **av)
{
    int i = 0;
    int  k;
    while (av[1][i])
    {
        k = '0';
        while (av[1][i] > k)
        {
            write(1, &av[1][i], 1);
                k++;
        }
        i++;
    }
}