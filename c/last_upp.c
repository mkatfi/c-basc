#include <unistd.h>

int	main(int ac, char **av)
{
	int i = 0;
	while (av[1][i])
	{
		if (av[1][i] >= 'A' && av[1][i] <= 'Z')
			av[1][i] += 32;
		i++;
	}
	i = 0;
	while (av[1][i])
	{
		if (av[1][i + 1] == 32 || !av[1][i + 1])
		{
			if (av[1][i] >= 'a' && av[1][i] <= 'z')
				av[1][i] -= 32;
		}
		write(1, &av[1][i], 1);
		i++;
	}
	write(1, "\n", 1);
}