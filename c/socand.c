#include <unistd.h>

int	main(int ac, char **av)
{
	int i;

	i = 0;
	while (av[1][i])
	{
		if (av[1][i] >= 'A' && av[1][i] <= 'Z')
			av[1][i] += 32;
		i++;
	}
	i = 0;
	while (av[1][i])
	{
		if ((av[1][0] >= 'a' && av[1][0] <= 'z'))
			if (av[1][1] >= 'a' && av[1][1] <= 'z')
			{
				av[1][1] -= 32;
			
			}
		if (av[1][i] == 32 && (av[1][i + 1] >= 'a' && av[1][i + 1] <= 'z'))
			if (av[1][i+2] >= 'a' && av[1][i + 2] <= 'z')
				{
					av[1][i + 2] -= 32;
					
				}
		write(1, &av[1][i], 1);
		i++;
	}
}